package com.commax.behaviorparam2;

public class Apple {
    String name;
    int weight;
    String color;

    public Apple(String name, int weight, String color) {
        this.name = name;
        this.weight = weight;
        this.color = color;
    }

    public String getName() {
        return name;
    }

    public int getWeight() {
        return weight;
    }

    public String getColor() {
        return color;
    }
}
